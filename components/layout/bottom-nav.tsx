interface BottomNavProps {
  role?: UserRole;
  userId?: string | null;
  initialUnreadCount?: number;
}

export default function BottomNav({
  role = "candidate",
  userId = null,
  initialUnreadCount = 0,
}: BottomNavProps) {
  const pathname = usePathname();
  const [unreadCount, setUnreadCount] = useState(initialUnreadCount);

  const navItems =
    role === "hr_manager" || role === "admin"
      ? hrNav
      : role === "employee"
      ? employeeNav
      : candidateNav;

  useEffect(() => {
    if (role !== "candidate" || !userId) {
      setUnreadCount(0);
      return;
    }

    const supabase = createClient();
    let isMounted = true;

    const refreshUnreadCount = async () => {
      const { count, error } = await supabase
        .from("notifications")
        .select("id", { count: "exact", head: true })
        .eq("recipient_id", userId)
        .eq("is_read", false);

      if (!isMounted || error) {
        return;
      }

      setUnreadCount(count ?? 0);
    };

    void refreshUnreadCount();

    const channel = supabase
      .channel(`notifications-${userId}`)
      .on(
        "postgres_changes",
        {
          event: "*",
          schema: "public",
          table: "notifications",
          filter: `recipient_id=eq.${userId}`,
        },
        () => {
          void refreshUnreadCount();
        }
      )
      .subscribe();

    return () => {
      isMounted = false;
      void supabase.removeChannel(channel);
    };
  }, [role, userId]);

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50 bg-white border-t border-gray-200">
      <div className="mx-auto max-w-[480px] flex items-center justify-around h-16">
        {navItems.map((item) => {
          const isActive =
            pathname === item.href ||
            (item.href !== "/dashboard" && pathname.startsWith(item.href));
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex flex-col items-center justify-center gap-1 min-w-[56px] py-2 text-xs transition-colors ${
                isActive
                  ? "text-blue-600"
                  : "text-gray-500 hover:text-gray-700"
              }`}
            >
              <span className="relative flex items-center justify-center">
                {item.icon}
                {item.showBadge && unreadCount > 0 && (
                  <span className="absolute -right-2 -top-1 min-w-5 rounded-full border border-gray-300 bg-gray-200 px-1.5 py-0.5 text-[10px] font-semibold leading-none text-gray-900">
                    {unreadCount > 99 ? "99+" : unreadCount}
                  </span>
                )}
              </span>
              <span>{item.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}